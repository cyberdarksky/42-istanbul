int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb > 2147483647 || nb < -2147483648)
		return (0);
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
